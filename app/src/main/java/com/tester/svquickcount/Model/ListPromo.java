package com.tester.svquickcount.Model;

public class ListPromo {
    String gambar;
    String id_promo;
    String judul;
    String deskripsi;
    String kodepromo;
    String tanggalberakhir;

    public String getId_promo() {
        return id_promo;
    }

    public void setId_promo(String id_promo) {
        this.id_promo = id_promo;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getKodepromo() {
        return kodepromo;
    }

    public void setKodepromo(String kodepromo) {
        this.kodepromo = kodepromo;
    }

    public String getTanggalberakhir() {
        return tanggalberakhir;
    }

    public void setTanggalberakhir(String tanggalberakhir) {
        this.tanggalberakhir = tanggalberakhir;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }
}
